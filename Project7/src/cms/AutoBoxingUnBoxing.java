package cms;

public class AutoBoxingUnBoxing {

	public static void main(String[] args) {
		int a=100;
		System.out.println(a);
		
		// Converting Primitive to object  // AutoBoxing
		Integer i=a;
		System.out.println(a);
		
		//Converting an aobject to primitive // AutoUnBoxing
		int x=i;
		System.out.println(x);
		
	}

}
